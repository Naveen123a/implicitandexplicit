package methods;
import java.io.*;
public class staticc {

	// Java Program to Illustrate Static Methods


	// Main class
	static class GFG {

		// Method 1
		// Static method
		static void hello()
		{

			// Print statement
			System.out.println("Hello");
		}

		// Method 2
		// Main driver method
		public static void main(String[] args)
		{

			// calling the Method 1
			// Accessing method
			hello();
		}
	}

}
